package conference.handle.bean;

public class Organizer {

	private int oid;
	private String o_userName;
	private String o_password;
	private String o_email;
	private String o_mobilePhone;
	public int getOid() {
		return oid;
	}
	public void setOid(int oid) {
		this.oid = oid;
	}
	public String getO_userName() {
		return o_userName;
	}
	public void setO_userName(String o_userName) {
		this.o_userName = o_userName;
	}
	public String getPassword() {
		return o_password;
	}
	public void setO_password(String o_password) {
		this.o_password = o_password;
	}
	public String getO_email() {
		return o_email;
	}
	public void setO_email(String o_email) {
		this.o_email = o_email;
	}
	public String getO_mobilePhone() {
		return o_mobilePhone;
	}
	public void setO_mobilePhone(String o_mobilePhone) {
		this.o_mobilePhone = o_mobilePhone;
	}
	
}
